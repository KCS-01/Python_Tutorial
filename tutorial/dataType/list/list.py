# 3. 리스트 연산하기

# 3.1 리스트 더하기
# 파이썬에서는 두 개의 리스트를 합치는 것이 가능하다

a = [1, 2, 3]
b = [4, 5, 6]

print(a + b)

# 3.2 리스트 반복하기
# 문자열과 마찬가지로 * 연산자를 이용해서 반복시킬 수 있다

print(a*2 + b*4)

# 3.3 리스트 길이구하기

length = len(a)
print(length)



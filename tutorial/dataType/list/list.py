# 1. 리스트 생성
# 파이썬에서는 리스트 안에는 어떠한 자료형도 포함될 수 있다
array = ['a', 'b', 'c', 'd', 'e']
print(array)

emptyArray = list()
print(emptyArray)

###################################################
#=================================================#
###################################################

# 2. 리스트의 인덱싱과 슬라이싱

indexing = array[3]
print(indexing)

# 3번 인덱스 제외 이전까지
slicing = array[:3]
print(slicing)

# 3번 인덱스 포함 이후까지
slicing2 = array[3:]
print(slicing2)


# 4. 리스트의 수정과 삭제

# 4.1 리스트의 값 수정하기
modify = [1, 2, 3, 4]
modify[2] = 5

print(modify)

# 4.2 del 함수를 사용해 리스트 요소 삭제하기

del modify[2]
print(modify)


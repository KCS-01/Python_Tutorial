# 문자열 자료형
# 연속된 문자들의 나열

# 1. 큰 따옴표로 둘러싸기
doubleQuotes = "Hello World"
print(doubleQuotes)

# 2. 작은 따옴표로 둘러싸기
singleQuote = 'Hello World'
print(singleQuote)

# 3. 큰 따옴표 3개를 연속으로 둘러싸기
doubleQuotesTriple = """Hello World"""
print(doubleQuotesTriple)

# 4. 작은 따옴표 3개를 연속으로 둘러싸기
singleQuoteTriple = '''Hello World'''
print(singleQuoteTriple)
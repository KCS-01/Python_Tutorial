# 문자열연산

text1 = "#파이썬"
text2 = "#웹"
text3 = "#크롤링"

total_text = text1 + text2 + text3

print(total_text)

total_text = text1 + " " + text2 + " "  + text3

print(total_text)

total_text = text1 + "\n" + text2 + "\n"  + text3

print(total_text)

total_text = text1 + "\n" + text2 + "\n"  + text3*3

print(total_text)


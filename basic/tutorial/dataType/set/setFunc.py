setAdd = set([1, 2, 3])
print(setAdd)
# {1, 2, 3}

setAdd.add(4)
print(setAdd)
# {1, 2, 3, 4}

setUpdate = set([1, 2, 3])
print(setUpdate)
# {1, 2, 3}

setUpdate.update([4, 5, 6])
print(setUpdate)
# {1, 2, 3, 4, 5, 6}

setRemove = set([1, 2, 3])
print(setRemove)
# {1, 2, 3}

setRemove.remove(2)
print(setRemove)
# {1, 3}